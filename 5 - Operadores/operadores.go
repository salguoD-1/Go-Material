package main

import (
	"fmt"
)

func main() {
	soma := 3 + 5
	subtracao := 10 - 3
	multiplicacao := 3 * 5
	divisao := 10 / 2
	modulo := 20 % 5

	fmt.Println("Soma: ", soma)
	fmt.Println("Subtração: ", subtracao)
	fmt.Println("Multiplicação: ", multiplicacao)
	fmt.Println("Divisão: ", divisao)
	fmt.Println("Módulo: ", modulo);
}